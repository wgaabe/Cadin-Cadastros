    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
        if(TTabelaClientes.getSelectedRow()>=0){
            if(JOptionPane.showConfirmDialog(this, "Deseja realmente Excluir ?")==0){
                CadastrarClientes cc = new CadastrarClientes();
                cc.excluirproduto(Integer.parseInt(""+TTabelaClientes.getValueAt(TTabelaClientes.getSelectedRow(), 0 )));
                limparTabela();
                buscarclientes();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um Produto");
        }
            
    }//GEN-LAST:event_BExcluirActionPerformed

