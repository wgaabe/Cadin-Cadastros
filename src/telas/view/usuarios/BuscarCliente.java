    public void limparTabela()
    {
        if(TTabelaClientes.getRowCount()>0)
        {
            for(int i=TTabelaClientes.getRowCount()-1; i>=0;i--)
            {
                ((DefaultTableModel) TTabelaClientes.getModel()).removeRow(0);
            }
        }
    }
    public void buscarclientes()
    {
        
        CadastrarClientes cc = new CadastrarClientes();
        List list = cc.read(TPesquisa.getText());
        
        
        int num=TTabelaClientes.getSelectedRowCount();
        DefaultTableModel dtm = (DefaultTableModel)TTabelaClientes.getModel();
        limparTabela();
        

        for(int i = 0; i<list.size(); i++){
            //String ferramenta="Sim";
            CadCliPOJO ccp = (CadCliPOJO)list.get(i);
            
            
            
            dtm.addRow(new Object[]{ccp.getCodigo(), ccp.getNome(), ccp.getRg()});
        }
    }
