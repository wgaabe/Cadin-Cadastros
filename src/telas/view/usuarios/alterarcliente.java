        CadCliPOJO ccp = new CadCliPOJO();
        CadCliMODEL ccm = new CadCliMODEL();
        CadastrarClientes cc = new CadastrarClientes();
        
        ccp.setCodigo(Integer.parseInt(TCodAlterarUsuario.getText()));
        ccp.setNome(TNomeAlterarUsuario.getText());
        ccp.setTelefone(Integer.parseInt(TTelefoneAlterarUsuario.getText()));
        ccp.setCpf(TCodAlterarUsuario.getText());
